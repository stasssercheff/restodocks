import sys
from datetime import date
from pathlib import Path

from fpdf import FPDF

FONT = "/System/Library/Fonts/Supplemental/Arial Unicode.ttf"
OUT = Path.home() / "Desktop" / "Restodocks_Produkt_demo.pdf"


class Doc(FPDF):
    def __init__(self) -> None:
        super().__init__()
        self.set_auto_page_break(auto=True, margin=14)
        self.add_font("RU", "", FONT)
        self.add_font("RU", "B", FONT)

    def title_block(self, text: str) -> None:
        self.set_font("RU", "B", 14)
        self.multi_cell(0, 7, text)
        self.ln(2)

    def h1(self, text: str) -> None:
        self.ln(2)
        self.set_font("RU", "B", 11.5)
        self.multi_cell(0, 5.5, text)
        self.ln(0.5)

    def h2(self, text: str) -> None:
        self.ln(1)
        self.set_font("RU", "B", 10)
        self.multi_cell(0, 5, text)
        self.ln(0.3)

    def p(self, text: str) -> None:
        self.set_font("RU", "", 9)
        self.multi_cell(0, 4.1, text)
        self.ln(0.6)

    def bullet(self, text: str) -> None:
        self.set_font("RU", "", 9)
        self.multi_cell(0, 4.1, "\u2022 " + text)
        self.ln(0.15)


