    pdf.bullet("Технологические карты: создание, редактирование, себестоимость, импорт Excel, экран согласования импорта.")
    pdf.bullet("Списания, инвентаризация (несколько режимов), расходы в т.ч. зарплата/ФОТ — с ограничениями тарифа где заложено.")
    pdf.bullet("Входящие: типизированные документы (заказы, списания, чеклисты, инвентаризация, приёмка, изменения ТТК, iiko и др.), личные и групповые чаты, вложения.")
    pdf.bullet("Чеклисты: из ТТК и произвольные, дедлайны, назначения; опционально черновик через ИИ.")
    pdf.bullet("Внутренняя документация заведения с ограничением аудитории.")
    pdf.bullet("HACCP: типизированные журналы, неизменяемые записи после сохранения, экспорт PDF с шаблонами по стране (RU, FDA, EU, UK, TR и др.).")
    pdf.bullet("POS-контур: заказы, KDS, склад, закупки, остатки, зал/столы, касса, отчёты, операции, экспорт продаж, сводки — с оговоркой по веб-редиректам.")
