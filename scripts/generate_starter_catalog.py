def category_for_name(name: str) -> str:
    n = (name or "").lower()
    for cat, pat in CAT_KEYWORDS:
        if re.search(pat, n):
            return cat
    return "misc"

def unit_norm(u: str) -> str:
    if not u:
        return "кг"
    u = u.lower().strip()
    if u in ("kg", "кг", "k"): return "кг"
    if u in ("l", "л", "ltr", "liter"): return "л"
    if u in ("pcs", "шт", "pc", "штук"): return "шт"
    if u in ("g", "г", "gr"): return "г"
    return "кг"

