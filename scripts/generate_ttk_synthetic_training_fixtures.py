from __future__ import annotations

import argparse
import csv
from pathlib import Path

from openpyxl import Workbook


def save_xlsx(path: Path, sheet_title: str, rows: list[list]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    wb = Workbook()
    ws = wb.active
    ws.title = sheet_title[:31]
    for r in rows:
        ws.append(r)
    wb.save(path)


def save_csv(path: Path, rows: list[list], delimiter: str = ",") -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.writer(f, delimiter=delimiter)
        w.writerows(rows)


def save_multi_sheet(path: Path, sheets: list[tuple[str, list[list]]]) -> None:
    wb = Workbook()
    for i, (title, rows) in enumerate(sheets):
        if i == 0:
            ws = wb.active
            ws.title = title[:31]
        else:
            ws = wb.create_sheet(title[:31])
        for r in rows:
            ws.append(r)
    wb.save(path)


def write_readme(out: Path, count_xlsx: int, count_csv: int, count_txt: int) -> None:
    text = f"""Синтетические ТТК (сгенерированы скриптом scripts/generate_ttk_synthetic_training_fixtures.py)

Счёт: xlsx≈{count_xlsx}, csv≈{count_csv}, txt≈{count_txt}

Как использовать
------------------
1. Импорт в приложении: режим «одна ТТК» для файлов synth__1card__*, «несколько ТТК» для synth__multi__*.
2. После проверки — «Создать все», чтобы записалось обучение по подписи заголовка.
3. Повторяйте для разных файлов — расширяется покрытие форматов.

Подробности: docs/LEARNING_TTK_VERIFY.md
"""
    (out / "README.txt").write_text(text, encoding="utf-8")


